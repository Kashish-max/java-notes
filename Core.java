// JVM(Java virtual machine) is subbset of JRE (Java Runtime Environment)
// JRE = JVM + Library Classes
// JDK = JRE + Development Tools
// Library Classes are APIs used when the code does not belong to the core java
// javac compiler compiles the code and generates the byte code with command javac fileName.java
// The byte code is executed by JVM inside JRE
// JVM converts the byte code into machine code and is platform dependent.
// Every machine has its own JVM and JRE